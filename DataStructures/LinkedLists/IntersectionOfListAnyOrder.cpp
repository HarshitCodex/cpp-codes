Node* findIntersection(Node* head1, Node* head2)
{
    Node *result=new Node(-1);
    Node *reshead=result;
    
    map<int,int> mmap;
    
    while(head1!=NULL) {
        mmap[head1->data]=1;
        head1=head1->next;
    }
    
    while(head2!=NULL) {
        if(mmap[head2->data]==1) {
            result->next=new Node(head2->data);
            result=result->next;
        }
        head2=head2->next;
    }
    
    Node *t=reshead->next;
    *reshead=*(reshead->next);
    delete(t);
    
    return reshead;
}
