/* Should return data of n'th node from the end of linked list.
*  head: head of the linked list
*  n: nth node from end to find
*/
int getNthFromLast(Node *head, int n)
{
    
       Node* temp = head;
       Node* tt = head;
       
       int fu=1;
       
       for(int i=0; i<n; i++) {
           if(temp==NULL) {
                fu=-1;
                break;
            }
            temp=temp->next;
            
       }
       
       if(fu==-1) {
           return -1;
       }
       
       while(temp!=NULL) {
           temp=temp->next;
           tt=tt->next;
       }
       
       return tt->data;
}
